import type { DailyEntry } from '../types'

// Helper to create a date string N days ago
const daysAgo = (n: number): string => {
  const d = new Date()
  d.setDate(d.getDate() - n)
  return d.toISOString().split('T')[0] // "2024-03-04"
}

